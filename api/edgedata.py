class edgedata:

    def __init__(self, src, dist, weight):
        self.src = src
        self.weight = weight
        self.dist = dist

    def get_src(self):
        return self. src

    def get_dist(self):
        return self. dist

    def get_weight(self):
        return self. weight

    def set_src(self, src):
        self.src = src

    def set_dist(self, dist):
        self.dist = dist

    def set_weight(self, weight):
        self.weight = weight